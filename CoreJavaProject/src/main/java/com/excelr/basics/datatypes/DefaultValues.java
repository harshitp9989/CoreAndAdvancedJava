package com.excelr.basics.datatypes;

public class DefaultValues {

	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	
	static char c;
	static String st;

	static boolean bl;
	
	public static void main(String[] args) {
		System.out.println("Default byte :"+b);
		System.out.println("Default short :"+s);
		System.out.println("Default int :"+i);
		System.out.println("Default long :"+l);
		System.out.println("Default float :"+f);
		System.out.println("Default double :"+d);
		System.out.println("Default char :"+c);
		System.out.println("Default String :"+st);
		System.out.println("Default boolean :"+bl);
	}
}
